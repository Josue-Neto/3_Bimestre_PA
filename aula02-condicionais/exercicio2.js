let nome= "Pedro";
const idade= 18;

if (nome = idade) {
    console.log(`Parabens ${nome} em completar seus ${idade} apartir de agora seu voto seu obrigatório, use com sabedoria ele.`)
} else {
    console.log(`${nome} ainda seu voto não é obrigatorio, quando completar ${idade} ele sera obrigatório.`)
}